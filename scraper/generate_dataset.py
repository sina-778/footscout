# ── Generate per-90 stats per position ────────────────────────────────────
def gen_stats(pos: str, is_elite: bool = False) -> dict:
    rng = np.random
    mult = 1.3 if is_elite else 1.0
    base = {
        "FW": dict(gls=0.42, ast=0.17, xg=0.40, npxg=0.36, xag=0.14,
                   sh=2.8, sot=1.1, touches=34, prog_carries=4.3, prog_passes=2.8,
                   tackles_tkl=0.9, int_=0.5, clr=0.25, pass_pct=74),
        "MF": dict(gls=0.16, ast=0.24, xg=0.13, npxg=0.12, xag=0.21,
                   sh=1.4, sot=0.45, touches=68, prog_carries=5.2, prog_passes=7.8,
                   tackles_tkl=2.4, int_=1.4, clr=0.6, pass_pct=87),
        "DF": dict(gls=0.04, ast=0.06, xg=0.04, npxg=0.03, xag=0.05,
                   sh=0.45, sot=0.12, touches=62, prog_carries=2.4, prog_passes=6.2,
                   tackles_tkl=3.3, int_=1.9, clr=4.8, pass_pct=84),
        "GK": dict(gls=0.0, ast=0.01, xg=0.0, npxg=0.0, xag=0.01,
                   sh=0.05, sot=0.0, touches=38, prog_carries=0.3, prog_passes=3.2,
                   tackles_tkl=0.1, int_=0.05, clr=1.2, pass_pct=62),
    }.get(pos, {})

    def n(mu, sd):
        return max(0.0, round(rng.normal(mu * mult, sd), 3))

    return {
        "gls_per90":           n(base["gls"],        0.12),
        "ast_per90":           n(base["ast"],         0.08),
        "xg_per90":            n(base["xg"],          0.10),
        "npxg_per90":          n(base["npxg"],        0.09),
        "xag_per90":           n(base["xag"],         0.07),
        "sh_per90":            n(base["sh"],           0.5),
        "sot_per90":           n(base["sot"],          0.28),
        "touches_per90":       n(base["touches"],      8.0),
        "prog_carries_per90":  n(base["prog_carries"], 0.8),
        "prog_passes_per90":   n(base["prog_passes"],  1.2),
        "tackles_tkl_per90":   n(base["tackles_tkl"],  0.5),
        "int_per90":           n(base["int_"],          0.3),
        "clr_per90":           n(base["clr"],           0.8),
        "pass_completion_pct": round(min(98, max(45, rng.normal(base["pass_pct"], 5))), 1),
    }


def assign_mv(name: str, pos: str, age: int) -> float:
    if name in MV:
        return MV[name]
    base = {"FW": 18e6, "MF": 14e6, "DF": 11e6, "GK": 7e6}.get(pos, 10e6)
    peak_factor = max(0.25, 1.2 - max(0, age - 25) * 0.07)
    return round(base * peak_factor * np.random.uniform(0.4, 2.2), -4)


# ── Build DataFrames ────────────────────────────────────────────────────────
fbref_rows, tm_rows = [], []
for name, squad, league, pos, age, nation in UNIQUE:
    is_elite = name in MV and MV[name] >= 60e6
    stats = gen_stats(pos, is_elite)
    mins = int(np.random.randint(900, 3300))
    mv = assign_mv(name, pos, age)
    
    is_wc = 1 if (name in world_cup_players or nation in WORLD_CUP_NATIONS) else 0
    
    fbref_rows.append({"player": name, "squad": squad, "league": league,
                        "pos": pos, "age": age, "nation": nation,
                        "is_world_cup": is_wc,
                        "playing_time_min": mins, **stats})
    tm_rows.append({"player": name, "squad": squad, "market_value_eur": mv,
                     "market_value_raw": f"€{mv/1e6:.1f}M", "position_detail": pos,
                     "age_tm": age, "nationality_tm": nation,
                     "contract_expires": f"202{np.random.randint(5, 9)}"})

fbref_df = pd.DataFrame(fbref_rows)
